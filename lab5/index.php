<?php
// №1 Определение времени года по месяцу
$month = 5; // Пример значения, замените на ваше

echo "<h2>№1 Определение времени года по месяцу</h2>";
if ($month == 12 || $month == 1 || $month == 2) {
    echo "Зима";
} elseif ($month >= 3 && $month <= 5) {
    echo "Весна";
} elseif ($month >= 6 && $month <= 8) {
    echo "Лето";
} elseif ($month >= 9 && $month <= 11) {
    echo "Осень";
} else {
    echo "Некорректное значение месяца";
}

echo "<br><br>";

// №2 Проверка первого символа строки
$string = 'abcde'; // Пример строки, замените на вашу

echo "<h2>№2 Проверка первого символа строки</h2>";
if ($string[0] === 'a') {
    echo "Первый символ - 'a'";
} else {
    echo "Первый символ не - 'a'";
}

echo "<br><br>";

// №3 Проверка первого символа числа
$number = 12345; // Пример числа, замените на ваше

echo "<h2>№3 Проверка первого символа числа</h2>";
$firstDigit = (string)$number[0]; // Преобразуем число в строку и берем первый символ

if ($firstDigit === '1' || $firstDigit === '2' || $firstDigit === '3') {
    echo "Первый символ - 1, 2 или 3";
} else {
    echo "Первый символ не 1, 2 или 3";
}

echo "<br><br>";

// №4 Нахождение суммы цифр трехзначного числа
$threeDigitNumber = 456; // Пример числа, замените на ваше

echo "<h2>№4 Нахождение суммы цифр трехзначного числа</h2>";
$digits = str_split($threeDigitNumber); // Преобразуем число в массив цифр
$sum = array_sum($digits); // Суммируем цифры

echo "Сумма цифр: $sum";

echo "<br><br>";

// №5 Проверка равенства суммы первых и вторых трех цифр шестизначного числа
$sixDigitNumber = 123321; // Пример числа, замените на ваше

echo "<h2>№5 Проверка равенства суммы первых и вторых трех цифр шестизначного числа</h2>";
$numberStr = str_pad($sixDigitNumber, 6, '0', STR_PAD_LEFT); // Убедимся, что число имеет 6 цифр

$firstHalf = substr($numberStr, 0, 3); // Первые три цифры
$secondHalf = substr($numberStr, 3, 3); // Последние три цифры

$sumFirstHalf = array_sum(str_split($firstHalf)); // Сумма первых трех цифр
$sumSecondHalf = array_sum(str_split($secondHalf)); // Сумма последних трех цифр

if ($sumFirstHalf === $sumSecondHalf) {
    echo "Сумма первых трех цифр равна сумме вторых трех цифр";
} else {
    echo "Сумма первых трех цифр не равна сумме вторых трех цифр";
}
?>
